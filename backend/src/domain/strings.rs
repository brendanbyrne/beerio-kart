//! Validated string newtypes.
//!
//! Companion to [`super::ids`] — where `ids.rs` makes "wrong-id-in-this-arg"
//! a compile error, this module makes "unvalidated-string-in-this-arg" a
//! compile error. Each newtype wraps a `String` and runs sanitization +
//! validation in its constructor, so a downstream caller that holds a
//! [`Username`] can trust it is 1–30 characters with no leading/trailing
//! whitespace without re-checking. See `coding-standards/rust.md` § 2.
//!
//! Constructors are the boundary: at HTTP request handlers, build the
//! newtype with `try_from(raw_string)?`, map the parse error to
//! [`Error::BadRequest`], and pass the newtype the rest of the way. At the
//! entity boundary (when reading a row), use the `from_db` helper to map a
//! constructor failure to [`Error::Internal`] — a stored value that fails
//! validation is data corruption, not a client error.
//!
//! Derive choices follow the pattern from [`super::ids`]: full derives on
//! types that flow through the API contract ([`Username`], [`EmailAddress`],
//! [`DrinkTypeName`], [`ImagePath`], [`RunNotes`]); restricted derives on
//! secrets ([`Password`], [`PasswordHash`]) to keep them out of logs and
//! wire payloads.

use nutype::nutype;

use crate::error::Error;

// ── Predicates and sanitizers (free functions for clarity) ───────────────

fn is_argon2_phc_hash(s: &str) -> bool {
    // PHC strings for any Argon2 variant. We only ever produce `$argon2id$`,
    // but accept all three so a hash generated by an older crate version
    // still parses. argon2's own `PasswordHash::new` performs the strict
    // parse downstream when verifying.
    s.starts_with("$argon2id$") || s.starts_with("$argon2i$") || s.starts_with("$argon2d$")
}

fn looks_like_email(s: &str) -> bool {
    // Intentionally minimal: RFC 5322 is famously not regex-checkable, and
    // the canonical answer is "send a confirmation email." We enforce the
    // bare minimum that catches obvious typos: exactly one `@`, with at
    // least one character on each side, and a `.` in the domain part.
    // Destructure-on-iterator avoids the per-call `Vec<&str>` allocation
    // that a `.collect()` form would carry.
    let mut parts = s.split('@');
    matches!(
        (parts.next(), parts.next(), parts.next()),
        (Some(local), Some(domain), None)
            if !local.is_empty() && !domain.is_empty() && domain.contains('.')
    )
}

// ── User-facing string newtypes ──────────────────────────────────────────

/// A user-chosen account name. 1–30 characters after trimming surrounding
/// whitespace.
///
/// `len_char_min`/`max` count Unicode scalar values (matching the existing
/// `username.chars().count()` check in the register handler). Trimming
/// happens before length validation, so `"  "` is rejected as empty rather
/// than passing as a 2-char username.
#[nutype(
    sanitize(trim),
    validate(len_char_min = 1, len_char_max = 30),
    derive(
        Debug,
        Clone,
        PartialEq,
        Eq,
        Hash,
        Display,
        AsRef,
        Serialize,
        Deserialize,
        TryFrom,
    )
)]
pub struct Username(String);

impl Username {
    /// Parse a username read from the database (typically `users.username`
    /// or a JOIN result aliasing it). Mirrors [`PasswordHash::from_db`] and
    /// [`ImagePath::from_db`]: a stored value that fails the newtype's
    /// invariant is data corruption, so the failure surfaces as
    /// [`Error::Internal`] rather than a client-facing `BadRequest`.
    ///
    /// # Errors
    ///
    /// Returns [`Error::Internal`] if the stored value fails the trim +
    /// 1–30 character check. The `column` argument is woven into the error
    /// context so logs name the source column (e.g.
    /// `"Invalid username in users.username"`).
    pub fn from_db(s: String, column: &'static str) -> Result<Self, Error> {
        Self::try_from(s).map_err(|e| {
            Error::Internal(
                anyhow::Error::msg(e.to_string()).context(format!("Invalid username in {column}")),
            )
        })
    }
}

/// An email address. Stored as the lowercased, trimmed form; rejection is
/// limited to obvious-shape checks (see [`looks_like_email`]). 3–320
/// characters (320 is the RFC 5321 upper bound for `local@domain`).
///
/// Currently unused on the write path — the `users.email` column is always
/// `None` at insert time — but defined here so that when the email-capture
/// flow lands the validation already exists at the boundary.
#[nutype(
    sanitize(trim, lowercase),
    validate(len_char_min = 3, len_char_max = 320, predicate = looks_like_email),
    derive(
        Debug,
        Clone,
        PartialEq,
        Eq,
        Hash,
        Display,
        AsRef,
        Serialize,
        Deserialize,
        TryFrom,
    ),
)]
pub struct EmailAddress(String);

/// A plaintext password as submitted by the user. 8–128 characters.
///
/// Deliberately *missing* `Debug`, `Display`, `Serialize`, and `Deserialize`
/// derives: passwords must never reach a log line, a `tracing` field, a
/// JSON response, or the bug-report attachment surface. The only way out
/// is the explicit [`into_inner`](Self::into_inner) call, which makes
/// leakage grep-visible in review.
///
/// Validation length matches the existing register/change-password checks
/// (`body.password.len() < 8 || > 128`). The original check counted bytes;
/// `len_char_min`/`max` count characters. For ASCII passwords the two are
/// identical; for multi-byte passwords the new behavior is slightly more
/// permissive (a 4-codepoint password with 4-byte glyphs was previously
/// allowed at 16 bytes but rejected at 4 chars — the char count is the
/// user-visible quantity and matches what password UIs typically display).
#[nutype(validate(len_char_min = 8, len_char_max = 128), derive(AsRef, TryFrom))]
pub struct Password(String);

/// An Argon2id password hash in PHC string format.
///
/// Constructed by [`crate::services::auth::hash_password`]; round-trips
/// through the `users.password_hash` column. The constructor only checks
/// the variant prefix — the full PHC parse happens in `verify_password`
/// via `argon2::password_hash::PasswordHash::new`, which produces a
/// targeted error if the body is malformed.
///
/// Like [`Password`], deliberately missing `Display` so the hash can't
/// accidentally land in a log message via `tracing::field::display`.
/// `Debug` is included because the hash isn't secret in the same way the
/// plaintext is — it's stored at rest and reproducible from the password —
/// and `Debug` is useful for assertion failures in tests.
#[nutype(
    validate(predicate = is_argon2_phc_hash),
    derive(Debug, Clone, AsRef, Serialize, Deserialize, TryFrom),
)]
pub struct PasswordHash(String);

impl PasswordHash {
    /// Parse a hash read from the `users.password_hash` column.
    ///
    /// # Errors
    ///
    /// Returns [`Error::Internal`] if the stored value doesn't start with a
    /// known Argon2 variant prefix. Data corruption only — should never
    /// happen, but we'd rather surface a clear 500 than attempt to verify
    /// against a malformed hash and get a confusing argon2 error later.
    pub fn from_db(s: String) -> Result<Self, Error> {
        Self::try_from(s).map_err(|e| {
            Error::Internal(
                anyhow::Error::msg(e.to_string()).context("Invalid hash in users.password_hash"),
            )
        })
    }
}

