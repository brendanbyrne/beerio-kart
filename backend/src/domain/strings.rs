pub struct DrinkTypeName(String);

/// A path to a static image asset (track preview, character portrait, etc.).
///
/// Populated by seed data, surfaced in `GET /api/v1/game-data/*` responses.
/// The newtype's job is mostly documentational at this stage — there's no
/// user-input path that constructs one — but it lets the response DTOs say
/// `image_path: ImagePath` instead of `image_path: String` and catches
/// hand-edited seed rows that omit the value.
///
/// `sanitize(trim) + len_char_min = 1` matches the shape used by
/// [`Username`] / [`DrinkTypeName`] / [`RunNotes`]: surrounding whitespace
/// is normalized, then the trimmed result must be non-empty. A corrupt
/// seed row like `"  /foo  "` becomes `"/foo"` rather than surviving with
/// the whitespace intact.
#[nutype(
    sanitize(trim),
    validate(len_char_min = 1, len_char_max = 500),
    derive(
        Debug,
        Clone,
        PartialEq,
        Eq,
        Hash,
        Display,
        AsRef,
        Serialize,
        Deserialize,
        TryFrom,
    )
)]
pub struct ImagePath(String);

impl ImagePath {
    /// Parse an image path read from a seeded lookup-table row.
    ///
    /// # Errors
    ///
    /// Returns [`Error::Internal`] if the stored path is empty or excessively
    /// long. Data corruption / mis-edited seed only.
    pub fn from_db(s: String, column: &'static str) -> Result<Self, Error> {
        Self::try_from(s).map_err(|e| {
            Error::Internal(
                anyhow::Error::msg(e.to_string())
                    .context(format!("Invalid image path in {column}")),
            )
        })
    }
}

/// Free-form notes on a run. Up to 2000 characters after trim.
///
/// Currently always `None` at insert time (the `runs.notes` column is
/// reserved for a future "add a note to your run" UI). Defined here so the
/// invariant ships with the newtype rather than appearing inline whenever
/// the feature lands.
#[nutype(
    sanitize(trim),
    validate(len_char_max = 2000),
    derive(
        Debug,
        Clone,
        PartialEq,
        Eq,
        Hash,
        Display,
        AsRef,
        Serialize,
        Deserialize,
        TryFrom,
    )
)]
pub struct RunNotes(String);

