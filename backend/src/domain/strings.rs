    #[test]
    fn test_username_deserializes_from_bare_string() {
        let json = "\"alice\"";
        let u: Username = serde_json::from_str(json).unwrap();
        assert_eq!(u.as_ref(), "alice");
    }

    #[test]
    fn test_username_deserialize_rejects_too_long() {
        let s = "a".repeat(31);
        let json = format!("\"{s}\"");
        let result: Result<Username, _> = serde_json::from_str(&json);
        result.unwrap_err();
    }

    // Property test: any string that passes Username's constructor round-trips
    // unchanged through serde JSON. The serde derive on a validated nutype
    // re-runs validation on deserialize, so a value built by `try_from` must
    // survive serialize → deserialize without mutation.
    proptest! {
        #[test]
        fn test_username_serde_round_trip(s in "[a-zA-Z0-9_]{1,30}") {
            let u = Username::try_from(s).unwrap();
            let json = serde_json::to_string(&u).unwrap();
            let recovered: Username = serde_json::from_str(&json).unwrap();
            prop_assert_eq!(u, recovered);
        }

        #[test]
        fn test_drink_type_name_serde_round_trip(s in "[a-zA-Z0-9 ]{1,50}") {
            // Skip cases where the input would be trimmed to empty (e.g. all
            // spaces); the regex above doesn't restrict surrounding spaces.
            prop_assume!(!s.trim().is_empty());
            let d = DrinkTypeName::try_from(s).unwrap();
            let json = serde_json::to_string(&d).unwrap();
            let recovered: DrinkTypeName = serde_json::from_str(&json).unwrap();
            prop_assert_eq!(d, recovered);
        }
    }
